
function color()
	{
		let color = prompt("Enter a background color:"," ");
		switch (color)
		{
			case "orange":
				document.body.bgColor="orange";
				break;
		
			case "yellow":
				document.body.bgColor="yellow";
				break;
			case "pink":
				document.body.bgColor="pink";
				break;
			case "blue":
				document.body.bgColor="blue";
				break;
			case "brown":
				document.body.bgColor="brown";
				break;
		}
	}
	